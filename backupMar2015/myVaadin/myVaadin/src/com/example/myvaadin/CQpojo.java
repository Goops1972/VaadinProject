package com.example.myvaadin;

public class CQpojo {

	private String cqid;
	private String cqName;
	private String cqDescription;
	private String bundleId;
	private String ruleId;
	
	
	
	
	
	
	public String getCqid() {
		return cqid;
	}
	public void setCqid(String cqid) {
		this.cqid = cqid;
	}
	public String getCqName() {
		return cqName;
	}
	public void setCqName(String cqName) {
		this.cqName = cqName;
	}
	public String getCqDescription() {
		return cqDescription;
	}
	public void setCqDescription(String cqDescription) {
		this.cqDescription = cqDescription;
	}
	public String getBundleId() {
		return bundleId;
	}
	public void setBundleId(String bundleId) {
		this.bundleId = bundleId;
	}
	public String getRuleId() {
		return ruleId;
	}
	public void setRuleId(String ruleId) {
		this.ruleId = ruleId;
	}
	
	
}
