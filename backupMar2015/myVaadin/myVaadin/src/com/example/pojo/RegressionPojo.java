package com.example.pojo;

public class RegressionPojo {

	private String bundleName;
	private int completePercent;
	private int hoursTaken;
	private int regressionType;
	public String getBundleName() {
		return bundleName;
	}
	public void setBundleName(String bundleName) {
		this.bundleName = bundleName;
	}
	public int getCompletePercent() {
		return completePercent;
	}
	public void setCompletePercent(int completePercent) {
		this.completePercent = completePercent;
	}
	public int getHoursTaken() {
		return hoursTaken;
	}
	public void setHoursTaken(int hoursTaken) {
		this.hoursTaken = hoursTaken;
	}
	public int getRegressionType() {
		return regressionType;
	}
	public void setRegressionType(int regressionType) {
		this.regressionType = regressionType;
	}


}
